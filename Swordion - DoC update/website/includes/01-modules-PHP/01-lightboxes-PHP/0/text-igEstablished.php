	<h2>Established</h2>

	<h3>The Digital Transform Office</h3>
	<p>The new Digital Transformation Office has been established to transform government service delivery to better meet the needs of all Australians.</p>

	<h3>Bureau of Communications Research</h3>
	<p>We established the Bureau of Communications Research to identify and analyse emerging market trends.</p>
	<p>The Bureau&rsquo;s work is improving our understanding of how the impacts of digitisation on productivity growth in the Australian economy are measured and understood. As part of this we have produced two Leading Indicators reports for the communications sector, a primer on digital productivity and two consultation papers.</p>

	<h3>Office of the Children&rsquo;s e-Safety Commissioner</h3>
	<p>The Government established the Office of the Children's eSafety Commissioner to provide national leadership in online safety for children and administer a complaints system for the rapid removal of cyber-bullying material from social media sites.</p>
