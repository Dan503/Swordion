    <p>The Capability Review was an independent, forward-looking assessment carried out by the Australian Public Service Commission (<?php infoTip('APSC', 'Australian Public Service Commission') ?> 
). It focused on how well placed we are to achieve our objectives and meet future challenges.</p>
    <p>The review was carried out soon after our new structure was put in place in September 2014 and proved to be a timely and valuable exercise, as it allowed us to test our progress and obtain an independent view of our strengths and development areas.</p>
    <p>The review found our Department has a range of strengths, including a collegiate, dedicated workforce with a strong delivery focus, positive stakeholder relationships and a clear strategy and purpose. It also provided direction on where we need to further focus our efforts to build capability, with key areas being leadership visibility and behaviours, people development, performance management and business planning.</p>
    <p>We used the review to shape our longer-term approach to strengthening leadership, strategy and delivery, and are implementing a Capability Action Plan in response to the review findings.</p>

