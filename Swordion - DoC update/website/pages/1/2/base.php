    <p>We started a significant change process in late February 2014 when our Corporate Plan for 2014&ndash;17 was released. Objectives included:</p>
    <ul>
        <li>align our structure and resources with our strategic priorities</li>
        <li>allow the Department to operate within available resourcing</li>
        <li>streamline decision making processes</li>
        <li>increase staff ownership of and accountability for their work</li>
        <li>strengthen our capabilities.</li>
    </ul>
    <p>A department-wide layer-by-layer design and selection process to redesign and fill positions in a new structure was carried out from March to August 2014.</p>
    <p>On 1 September 2014, we moved to the new structure that aligns our people and our resources to our new strategic priorities.&nbsp;The structure includes:</p>
    <ul>
        <li>a strategy office to provide advice and coordination</li>
        <li>three policy areas</li>
        <li>the Bureau of Communications Research</li>
        <li>a Corporate Division</li>
        <li>an Office of the General Counsel.</li>
    </ul>

    <h3>Australian Public Service Commission Capability Review</h3>
    <p>The Capability Review was an independent, forward-looking assessment carried out by the Australian Public Service Commission (<?php infoTip('APSC', 'Australian Public Service Commission') ?> 
). It focused on how well placed we are to achieve our objectives and meet future challenges.</p>
    <p>The review was carried out soon after our new structure was put in place in September 2014 and proved to be a timely and valuable exercise, as it allowed us to test our progress and obtain an independent view of our strengths and development areas.</p>
    <p>The review found our Department has a range of strengths, including a collegiate, dedicated workforce with a strong delivery focus, positive stakeholder relationships and a clear strategy and purpose. It also provided direction on where we need to further focus our efforts to build capability, with key areas being leadership visibility and behaviours, people development, performance management and business planning.</p>
    <p>We used the review to shape our longer-term approach to strengthening leadership, strategy and delivery, and are implementing a Capability Action Plan in response to the review findings.</p>

    <h3>Strengthening organisational capability</h3>
    <p>To realise the full benefits of the reorganisation and restructure, and support the implementation of our Corporate Plan strategic priorities, we identified a range of priority initiatives focused on building organisational capability.</p>
    <p>The Secretary created the Strengthening Organisational Capability Programme Office in January 2015 as a temporary measure to oversee, report on and communicate about the progress of those initiatives. The Programme Office also developed a Capability Action Plan in response to the APSC Capability Review Report that was publicly released on 2 June 2015.</p>
    <p>To improve our flexibility and agility when responding to changing priorities and landscape, we created a cross-departmental Project Office.&nbsp;It is led by an <?php infoTip('SES', 'Senior Executive Service') ?> 
 Band 1 officer and has the flexibility to take on emerging priorities that need to be resourced quickly, but have a finite life.&nbsp;The Project Office is largely staffed on a rotation basis, providing an opportunity for staff to develop their skills and capabilities and contribute to high priority work.&nbsp;</p>
    <p>We have made significant progress towards becoming a new Department of Communications in 2014&ndash;15, while also successfully delivering a major policy and programme agenda through the hard work, energy and enthusiasm of staff.</p>

