<p>Deploying the National Broadband Network through a multi-technology mix&mdash;a combination of fibre to the premises, fibre to the node, hybrid-fibre coaxial, fixed wireless and satellite technologies&mdash;provides $18 billion in benefits to the community and the economy. This was the key finding of a cost-benefit analysis report released in 2014&ndash;15 after a year-long industry review by a panel of experts.</p>
<p>Commissioned by the Department in December 2013, the panel was headed by Dr Michael Vertigan AC and tasked with carrying out an independent cost-benefit analysis of the National Broadband Network. It used innovative analytical techniques to examine the costs and benefits of different approaches to providing fast broadband in Australia, and these provided valuable insights.</p>
<p>The panel considered preferences and potential future demand for high-speed broadband throughout the country, and estimated how willing consumers would be to pay for increased broadband speeds. The panel also looked at the future competition in the telecommunications sector and the structural and regulatory framework for it.</p>
<p>In total, the panel made 53 recommendations and provided three reports to the Government:</p>
<ul>
    <li>A statutory review of Part XIC of the <em>Competition and Consumer Act 2010</em>, which sets out how telecommunications providers can make use of each other&rsquo;s networks.</li>
    <li>A cost-benefit analysis report, which compared the costs and benefits of alternative options for delivering high-speed broadband to Australian households and businesses.</li>
    <li>A review examining the most appropriate overall structure and regulatory framework for Australia&rsquo;s future broadband market.</li>
</ul>
<p>We supported the panel in carrying out its review and helped it prepare and release its reports. We then helped the Government prepare its response, which was set out in its Telecommunications Regulatory and Structural Reform statement of December 2014. The statement set out a practical plan for completing the rollout of Australia&rsquo;s new broadband network as soon and cost-effectively as possible, while also outlining the long-term strategy for supporting greater competition in the telecommunications sector.</p>
<p>Key measures in the statement included:</p>
<ul>
    <li>Requiring new high-speed fixed-line broadband networks serving residential customers to be structurally separated or functionally separated.</li>
    <li>Implementing a competitively neutral industry contribution mechanism for funding non-commercial National Broadband Network services.</li>
    <li>Accounting and systems separation arrangements to allow for the future break-up of NBN Co into competing business units if it is considered appropriate in the long term.</li>
    <li>Managing interference and co-existence between competing Very-High-Bit-Rate Digital Subscriber Line 2 systems to optimise competition and performance.</li>
    <li>Legislating Infrastructure Provider of Last Resort obligations for NBN Co.</li>
</ul>
<p>The Department is progressing with the agreed initiatives so reforms can be fully implemented by 1 January 2017.</p>

